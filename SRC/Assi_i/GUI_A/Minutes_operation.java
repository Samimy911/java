    private void processActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processActionPerformed
        String input = Minutes.getText().trim();

        // Check if empty or contains non-digit characters
        if (input.isEmpty() || !input.matches("\\d+")) {
            Hours.setText("");
            Min.setText("");
            JOptionPane.showMessageDialog(this, 
                "Please enter a valid number of minutes (only digits).", 
                "Invalid Input", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Safe to convert â€“ we already checked it's only digits
        int totalMinutes = Integer.parseInt(input);
        int hours = totalMinutes / 60;
        int remainingMinutes = totalMinutes % 60;

        Hours.setText(String.valueOf(hours));
        Min.setText(String.valueOf(remainingMinutes));
    
    }//GEN-LAST:event_processActionPerformed

